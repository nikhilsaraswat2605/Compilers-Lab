int main()
{
    // examples of single and multi dimensional arrays
    int a[10], b[10][10], c[10][10][10], d[10][10][10][10];
    int *p, *q, *r, *s;
    p = &a[0];
    q = &b[0][0];
    r = &c[0][0][0];
    s = &d[0][0][0][0];
    a[0] = 1;
    b[0][0] = 2;
    c[0][0][0] = 3;
    d[0][0][0][0] = 4;
    char x[10], y[10][10], z[10][10][10];
    char **p1, *q1, ***r1;
    p1 = &x;
    q1 = &y[0][0];
    r1 = &z;
    x[0] = 'a';
    y[0][0] = 'b';
    z[0][0][0] = 'c';
    float f[10], g[10][10], h[10][10][10];
    float **p2, *q2, ***r2;
    p2 = &f;
    q2 = &g[0][0];
    r2 = &h;
    f[0] = 1.0 * f[1];
    g[0][0] = 2.0 * g[1][1];

    return 0;
}
